#!/usr/bin/env node

/**
 * Test Environment Setup Script
 *
 * This script helps you set up a dedicated test database for E2E tests.
 * It will guide you through creating a Supabase test project and configuring .env.test.
 */

const readline = require('readline');
const fs = require('fs');
const path = require('path');

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

const question = (query) => new Promise((resolve) => rl.question(query, resolve));

async function main() {
  console.log('\n🧪 === Test Environment Setup ===\n');
  console.log('This script will help you configure a dedicated test database for E2E tests.\n');

  console.log('STEP 1: Create a Supabase Test Project');
  console.log('----------------------------------------');
  console.log('1. Go to https://supabase.com/dashboard');
  console.log('2. Click "New Project"');
  console.log('3. Name it "apex-test" (or similar)');
  console.log('4. Choose a region close to you');
  console.log('5. Set a database password (save it!)');
  console.log('6. Wait for project to finish setting up (~2 minutes)\n');

  const ready = await question('Have you created the test project? (y/n): ');
  if (ready.toLowerCase() !== 'y') {
    console.log('\n❌ Please create the test project first, then run this script again.\n');
    rl.close();
    return;
  }

  console.log('\nSTEP 2: Get Your API Credentials');
  console.log('----------------------------------');
  console.log('1. In your Supabase test project dashboard');
  console.log('2. Go to Project Settings → API');
  console.log('3. Copy the following values:\n');

  const projectUrl = await question('Project URL (https://xxx.supabase.co): ');
  const anonKey = await question('anon/public key: ');
  const serviceKey = await question('service_role key (secret!): ');

  console.log('\nSTEP 3: Set Base Site URL');
  console.log('--------------------------');
  const siteUrl = await question('Test site URL (default: http://localhost:3050): ') || 'http://localhost:3050';

  console.log('\nSTEP 4: Creating .env.test file...');

  const envTestContent = `# Test Environment Configuration
# Auto-generated by setup-test-env.js on ${new Date().toISOString()}

# Site Configuration
NEXT_PUBLIC_SITE_URL="${siteUrl}"

# Supabase Test Database
NEXT_PUBLIC_SUPABASE_URL="${projectUrl}"
NEXT_PUBLIC_SUPABASE_ANON_KEY="${anonKey}"
SUPABASE_SERVICE_ROLE_KEY="${serviceKey}"

# Test-Specific Settings
NODE_ENV="test"
NEXT_PUBLIC_ENV="test"

# Optional: Email (use test mode or skip)
RESEND_API_KEY="test-key-or-real-key"

# Optional: Stripe (use test mode)
STRIPE_SECRET_KEY="sk_test_your_test_key"
STRIPE_WEBHOOK_SECRET="whsec_test_your_webhook_secret"

# Optional: VAPI (use test credentials or skip)
VAPI_API_KEY="test-vapi-key"
VAPI_PHONE_NUMBER="+15551234567"

# Optional: Twilio (use test credentials or skip)
TWILIO_ACCOUNT_SID="test-account-sid"
TWILIO_AUTH_TOKEN="test-auth-token"
TWILIO_PHONE_NUMBER="+15551234567"
`;

  const envTestPath = path.join(process.cwd(), '.env.test');
  fs.writeFileSync(envTestPath, envTestContent);

  console.log(`✅ Created .env.test at: ${envTestPath}\n`);

  console.log('STEP 5: Run Database Migrations');
  console.log('--------------------------------');
  console.log('Your test database needs the same schema as production.\n');
  console.log('Run these commands:');
  console.log('  npm run db:push     # Push schema to test database');
  console.log('  npm run db:seed     # (Optional) Seed test data\n');

  console.log('STEP 6: Run Tests');
  console.log('-----------------');
  console.log('  npm test            # Run all tests');
  console.log('  npm run test:e2e    # Run E2E tests only');
  console.log('  npm run test:unit   # Run unit tests only\n');

  console.log('✨ Setup complete!');
  console.log('\n⚠️  IMPORTANT SECURITY NOTES:');
  console.log('  - .env.test is in .gitignore (keep it secret!)');
  console.log('  - Use a SEPARATE Supabase project for testing');
  console.log('  - Never use production credentials in .env.test');
  console.log('  - Test database will be reset frequently\n');

  rl.close();
}

main().catch((error) => {
  console.error('❌ Error:', error);
  rl.close();
  process.exit(1);
});
